// ─────────────────────────── slug ───────────────────────────────────────────

/** Turn a brand name (or any label) into a filesystem-safe slug. */
export function slugify(input: string): string {
  const s = input
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[̀-ͯ]/g, "") // strip combining marks
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
  return s || "brand";
}

function normalizeBrandForAssembly(brand: Brand): Brand {
  const voice = brand.voice ?? {};
  const imagery = brand.imagery ?? {};
  const layout = brand.layout ?? {};
  return {
    ...brand,
    logo: {
      primary: null,
      alternates: [],
      notes: "",
      ...(brand.logo ?? {}),
    },
    colors: Array.isArray(brand.colors) ? brand.colors : [],
    typography: brand.typography ?? {},
    voice: {
      adjectives: Array.isArray(voice.adjectives) ? voice.adjectives : [],
      tone: voice.tone ?? "",
      messagingPillars: Array.isArray(voice.messagingPillars) ? voice.messagingPillars : [],
      vocabulary: {
        use: Array.isArray(voice.vocabulary?.use) ? voice.vocabulary.use : [],
        avoid: Array.isArray(voice.vocabulary?.avoid) ? voice.vocabulary.avoid : [],
      },
    },
    imagery: {
      style: imagery.style ?? "",
      subjects: Array.isArray(imagery.subjects) ? imagery.subjects : [],
      treatment: imagery.treatment ?? "",
      avoid: Array.isArray(imagery.avoid) ? imagery.avoid : [],
      samples: Array.isArray(imagery.samples) ? imagery.samples : [],
    },
    layout: {
      radius: layout.radius ?? "",
      borderWeight: layout.borderWeight ?? "",
      spacing: layout.spacing ?? "",
      postureRules: Array.isArray(layout.postureRules) ? layout.postureRules : [],
    },
  };
}

// ─────────────────────────── artifact kinds shipped by default ──────────────

/** The artifact products written into every bundle's artifacts/ folder. */
const ARTIFACT_KINDS: AssetKind[] = ["landing", "deck", "poster", "email", "newsletter", "form"];

// ─────────────────────────── the BRAND-SYSTEM.md doc ────────────────────────

function brandSystemDoc(
  slug: string,
  brand: Brand,
  seed: SeedToken,
  tokens: DesignTokens,
  tokenCount: number,
): string {
  return `# ${brand.name} — brand system

Generated by the branding-agent engine from a single **seed** (${Object.keys(seed).length} fields).
Everything below is *derived* — no token here was hand-authored.

- **slug:** \`${slug}\`
- **source:** ${brand.sourceUrl || "(none)"}
- **primary seed color:** \`${seed.colorPrimary}\`
- **derived tokens per theme:** ${tokenCount}
- **themes:** default · dark · compact (same seed, different algorithm)

## Files

| Path | What it is |
| --- | --- |
| \`seed.json\` | The ~20-field SeedToken — the only authored surface. |
| \`tokens.default.json\` | Full derived DesignTokens (light). |
| \`tokens.dark.json\` | Same seed, dark algorithm. |
| \`tokens.compact.json\` | Same seed, compact (dense) algorithm. |
| \`variables.css\` | \`:root{}\` + \`.dark{}\` CSS custom properties (\`--brand-*\`). |
| \`variables.dark.css\` | Standalone \`:root{}\` for the dark theme. |
| \`theme.json\` | antd \`ConfigProvider\` theme (token + algorithm). |
| \`kit.html\` | Themed component showcase (light). |
| \`kit.dark.html\` | Themed component showcase (dark). |
| \`artifacts/landing.html\` | Full landing page (hero → features → pricing → FAQ → CTA). |
| \`artifacts/deck.html\` | 9-slide 16:9 pitch deck with keyboard navigation. |
| \`artifacts/poster.html\` | 3:4 print-style poster. |
| \`artifacts/email.html\` | A marketing email. |
| \`artifacts/newsletter.html\` | Multi-story digest email. |
| \`artifacts/form.html\` | Split-screen contact / signup page. |
| \`index.html\` | Gallery linking everything + live component preview. |

## How to re-theme

The whole system traces back to \`seed.json\`. To re-brand, change one field and
re-run the engine — every downstream token, component and artifact follows.

- **Change the brand color:** edit \`colorPrimary\` in the seed (e.g. \`"#1677ff"\`).
  The 10-step palette, all interaction states, the primary background/border and
  every component that references \`var(--brand-color-primary)\` update together.
- **Light → dark:** the dark theme is the same seed run through the \`"dark"\`
  algorithm — load \`variables.dark.css\` (or add the \`.dark\` class).
- **Density:** the \`"compact"\` algorithm tightens spacing & control heights
  while keeping colors identical.

## Add a new artifact type

\`renderArtifact(kind, brand, tokens)\` already accepts every \`AssetKind\`
(\`landing | deck | poster | email | newsletter | form\`). Add the kind to the
build's artifact list to ship it in the bundle; kinds without a bespoke layout
fall back to a sensible card+button page.
`;
}

// ─────────────────────────── core assembly ──────────────────────────────────

interface AssembleInput {
  slug: string;
  brand: Brand;
  seed: SeedToken;
  /** Extra files (e.g. fetched logos) to merge into the bundle. */
  extraFiles?: Record<string, string>;
  /** Self-hosted webfonts (brand dir's fonts/manifest.json). When present,
   *  every rendered document gets matching @font-face rules inlined, with
   *  urls relative to its own location. */
  fontFiles?: FontFile[];
  /** Path from the bundle root to the directory holding fonts/ — `"../"` for
   *  the workspace layout (bundle written to <brandDir>/system/, fonts/ next
   *  to it) and `"./"` when fonts/ ships inside the bundle itself. */
  fontsBase?: string;
}

/** Derive all three themes from one seed and lay out the complete file bundle. */
function assemble({ slug, brand, seed, extraFiles, fontFiles, fontsBase = "../" }: AssembleInput): BrandSystem {
  const themes: Record<ThemeAlgorithm, DesignTokens> = {
    default: deriveTokens(seed, "default"),
    dark: deriveTokens(seed, "dark"),
    compact: deriveTokens(seed, "compact"),
  };

  const files: Record<string, string> = {};

  // ── seed + raw tokens ──
  files["seed.json"] = JSON.stringify(seed, null, 2);
  files["tokens.default.json"] = tokensToJson(themes.default);
  files["tokens.dark.json"] = tokensToJson(themes.dark);
  files["tokens.compact.json"] = tokensToJson(themes.compact);

  // ── CSS custom properties ──
  // variables.css carries the light theme on :root plus the dark theme scoped
  // under .dark, so a single stylesheet can drive both via a class toggle.
  files["variables.css"] =
    tokensToCssVars(themes.default, ":root") + "\n" + tokensToCssVars(themes.dark, ".dark");
  files["variables.dark.css"] = tokensToCssVars(themes.dark, ":root");
  files["scripts/apply-design-tokens.mjs"] = applyDesignTokensScript();

  // ── antd ConfigProvider theme ──
  files["theme.json"] = tokensToThemeJson(seed, "default");

  // kit/index sit at the bundle root, artifacts one level deeper — each doc's
  // @font-face urls are relative to its own location.
  const fontsPrefix = (depth: 1 | 2) => (depth === 1 ? `${fontsBase}fonts/` : `../${fontsBase}fonts/`);
  const withFonts = (html: string, depth: 1 | 2) =>
    injectFontFaces(html, fontFiles ?? [], fontsPrefix(depth));

  // ── themed component kit ──
  const fonts = brandFontAssets(brand);
  files["kit.html"] = withFonts(
    renderKitPage(themes.default, {
      title: `${brand.name} — component kit`,
      brandName: brand.name,
      fontLinks: fonts.links,
      displayFamily: fonts.displayFamily,
    }),
    1,
  );
  files["kit.dark.html"] = withFonts(
    renderKitPage(themes.dark, {
      title: `${brand.name} — component kit (dark)`,
      brandName: brand.name,
      fontLinks: fonts.links,
      displayFamily: fonts.displayFamily,
    }),
    1,
  );

  // ── artifacts (products) ──
  for (const kind of ARTIFACT_KINDS) {
    files[`artifacts/${kind}.html`] = withFonts(renderArtifact(kind, brand, themes.default), 2);
  }

  // ── gallery / index ──
  // srcdoc previews are separate documents — each gets its own injection,
  // with urls resolved from the index's location (depth 1).
  files["index.html"] = withFonts(
    buildIndexPage(slug, brand, themes.default, fontFiles, fontsPrefix(1)),
    1,
  );

  // ── docs ──
  const tokenCount = tokensToCssVars(themes.default).split("\n").filter((l) => l.includes("--brand-")).length;
  files["BRAND-SYSTEM.md"] = brandSystemDoc(slug, brand, seed, themes.default, tokenCount);

  // ── merge any extras (logos etc.) ──
  if (extraFiles) Object.assign(files, extraFiles);

  return { slug, seed, themes, files };
}

function applyDesignTokensScript(): string {
  return `#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const target = process.argv[2];
if (!target) {
  console.error('Usage: node scripts/apply-design-tokens.mjs <target-css-path>');
  process.exit(1);
}

const here = path.dirname(fileURLToPath(import.meta.url));
const source = path.resolve(here, '..', 'variables.css');
const destination = path.resolve(process.cwd(), target);
fs.mkdirSync(path.dirname(destination), { recursive: true });
fs.copyFileSync(source, destination);
console.log(\`Copied design tokens to \${destination}\`);
`;
}

// ─────────────────────────── index page (gallery) ───────────────────────────

/**
 * The bundle's index.html: links every produced file and embeds the artifact
 * gallery (live iframe previews of landing / email / poster). Built off the
 * same DesignTokens so it is themed like everything else.
 */
function buildIndexPage(
  slug: string,
  brand: Brand,
  tokens: DesignTokens,
  fontFiles?: FontFile[],
  fontsPrefix = "../fonts/",
): string {
  const esc = (v: string) =>
    v.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");

  const links = [
    ["kit.html", "Component kit (light)"],
    ["kit.dark.html", "Component kit (dark)"],
    ["artifacts/landing.html", "Landing page"],
    ["artifacts/email.html", "Email"],
    ["artifacts/poster.html", "Poster"],
    ["tokens.default.json", "tokens.default.json"],
    ["tokens.dark.json", "tokens.dark.json"],
    ["tokens.compact.json", "tokens.compact.json"],
    ["variables.css", "variables.css"],
    ["theme.json", "theme.json (antd)"],
    ["seed.json", "seed.json"],
    ["BRAND-SYSTEM.md", "BRAND-SYSTEM.md"],
  ]
    .map(
      ([href, label]) =>
        `<li><a href="${esc(href)}" style="color:var(--brand-color-link);text-decoration:none;">${esc(
          label,
        )}</a></li>`,
    )
    .join("\n");

  // renderArtifactGallery already returns a full standalone document themed by
  // the tokens; we prepend a header + file index section into its <body>.
  const gallery = renderArtifactGallery(brand, tokens, {
    decorate: (html) => injectFontFaces(html, fontFiles ?? [], fontsPrefix),
  });
  const fileIndex = `
    <section style="max-width:1120px;margin:0 auto;padding:0 var(--brand-size-xl) var(--brand-size-xl);">
      <h2 style="font-size:var(--brand-font-size-heading-3);margin-bottom:var(--brand-size-sm);color:var(--brand-color-text);">Files in this brand system</h2>
      <ul style="columns:2;gap:var(--brand-size-xl);font-size:var(--brand-font-size);line-height:1.9;list-style:none;padding:0;margin:0;">
${links}
      </ul>
      <p style="margin-top:var(--brand-size-lg);color:var(--brand-color-text-tertiary);font-size:var(--brand-font-size-sm);">slug: <code>${esc(
        slug,
      )}</code> · everything derived from <code>seed.json</code></p>
    </section>`;

  // Inject the file index right after the opening <body> of the gallery doc.
  return gallery.replace(/<body>/, `<body>\n${fileIndex}`);
}

// ─────────────────────────── public: from a Brand kit ───────────────────────

/**
 * Build a complete BrandSystem from an already-synthesized Brand kit. Fully
 * deterministic and offline.
 */
export function buildBrandSystem(
  brand: Brand,
  opts?: { slug?: string; fontFiles?: FontFile[] },
): BrandSystem {
  const normalizedBrand = normalizeBrandForAssembly(brand);
  const slug = opts?.slug ? slugify(opts.slug) : slugify(normalizedBrand.name);
  const seed = seedFromBrand(normalizedBrand);
  return assemble({ slug, brand: normalizedBrand, seed, fontFiles: opts?.fontFiles });
}

// ─────────────────────────── public: from a URL ─────────────────────────────

/**
 * Synthesize a minimal Brand from prefetched material so the URL path can drive
 * the same artifact/kit assembly. This is intentionally lightweight: the rich
 * Brand kit (voice, imagery, full color roles) is the LLM's job; here we only
 * need enough to seed colors/fonts/copy deterministically.
 */
function brandFromMaterial(material: PrefetchResult, seed: SeedToken): Brand {
  const hostName = (() => {
    try {
      const core = new URL(material.url).hostname.replace(/^www\./i, "").split(".")[0] ?? "";
      return core ? core.charAt(0).toUpperCase() + core.slice(1) : "";
    } catch {
      return "";
    }
  })();
  const name = (material.siteName || material.title || hostName || "Brand").trim();
  const headings = material.headings ?? [];
  const paragraphs = material.paragraphs ?? [];

  return {
    name,
    tagline: material.description || headings[0] || "",
    description: paragraphs[0] || material.description || "",
    sourceUrl: material.finalUrl || material.url,
    logo: {
      primary: material.logos?.[0] ? `logos/${material.logos[0].file}` : null,
      alternates: (material.logos ?? []).slice(1).map((l) => `logos/${l.file}`),
      notes: "",
    },
    colors: [
      { role: "background", hex: seed.colorBgBase, oklch: "", name: "background", usage: "page canvas" },
      { role: "foreground", hex: seed.colorTextBase, oklch: "", name: "foreground", usage: "body text" },
      { role: "accent", hex: seed.colorPrimary, oklch: "", name: "accent", usage: "primary brand color" },
    ],
    typography: {
      display: { family: material.fonts?.[0]?.family ?? "Inter", fallbacks: [], weights: [400, 700] },
      body: { family: material.fonts?.[0]?.family ?? "Inter", fallbacks: [], weights: [400, 700] },
    },
    voice: {
      adjectives: [],
      tone: "",
      messagingPillars: headings.slice(0, 3),
      vocabulary: { use: [], avoid: [] },
    },
    imagery: { style: "", subjects: [], treatment: "", avoid: [] },
    layout: {
      radius: `${seed.borderRadius}px`,
      borderWeight: `${seed.lineWidth}px`,
      spacing: "8px baseline grid",
      postureRules: [],
    },
  };
}

/**
 * Build a BrandSystem straight from a site URL — the deterministic, no-LLM
 * path. `prefetchBrand` must write to a brand dir, so we hand it a throwaway
 * temp dir, read back any downloaded logos into the bundle, then assemble.
 */
export async function buildFromUrl(url: string, opts?: { slug?: string }): Promise<BrandSystem> {
  const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), "brand-prefetch-"));
  try {
    const material = await prefetchBrand(url, tmpDir);
    if (!material) {
      throw new Error(`Could not fetch ${url} — the site may block server-side requests.`);
    }

    const seed = seedFromMaterial(material);
    const brand = brandFromMaterial(material, seed);
    const slug = opts?.slug ? slugify(opts.slug) : slugify(brand.name);

    // Pull any downloaded logos back into the in-memory bundle.
    const extraFiles: Record<string, string> = {};
    for (const logo of material.logos ?? []) {
      const abs = path.join(tmpDir, "logos", logo.file);
      try {
        if (logo.contentType?.includes("svg") || logo.file.endsWith(".svg")) {
          extraFiles[`logos/${logo.file}`] = fs.readFileSync(abs, "utf8");
        } else {
          // Binary assets: keep as base64 data so the bundle stays string-only.
          const b64 = fs.readFileSync(abs).toString("base64");
          extraFiles[`logos/${logo.file}.b64`] = b64;
        }
      } catch {
        /* logo missing on disk — skip */
      }
    }
    // Same for downloaded webfonts (binary → base64, manifest/css as text).
    for (const f of material.fontFiles ?? []) {
      try {
        extraFiles[`fonts/${f.file}.b64`] = fs
          .readFileSync(path.join(tmpDir, "fonts", f.file))
          .toString("base64");
      } catch {
        /* font missing on disk — skip */
      }
    }
    for (const aux of ["manifest.json", "fonts.css"]) {
      try {
        extraFiles[`fonts/${aux}`] = fs.readFileSync(path.join(tmpDir, "fonts", aux), "utf8");
      } catch {
        /* no fonts harvested */
      }
    }

    // fonts/ ships inside this standalone bundle, so urls resolve from its root.
    return assemble({ slug, brand, seed, extraFiles, fontFiles: material.fontFiles, fontsBase: "./" });
  } finally {
    fs.rmSync(tmpDir, { recursive: true, force: true });
  }
}

// ─────────────────────────── disk writer (shared by CLI + API) ──────────────

/**
 * Write a BrandSystem's files to disk under `outDir`. Binary logo files stored
 * as `*.b64` are decoded back to their real bytes. Returns the absolute paths
 * written, sorted.
 */
export function writeBrandSystem(system: BrandSystem, outDir: string): string[] {
  const written: string[] = [];
  for (const [rel, content] of Object.entries(system.files)) {
    const isB64 = rel.endsWith(".b64");
    const targetRel = isB64 ? rel.slice(0, -4) : rel;
    const abs = path.join(outDir, targetRel);
    fs.mkdirSync(path.dirname(abs), { recursive: true });
    if (isB64) {
      fs.writeFileSync(abs, Buffer.from(content, "base64"));
    } else {
      fs.writeFileSync(abs, content, "utf8");
    }
    written.push(abs);
  }
  return written.sort();
}
